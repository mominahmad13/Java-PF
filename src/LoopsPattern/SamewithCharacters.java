package LoopsPattern;
public class SamewithCharacters {
    public static void main(String[] args) {
        char n = 'E';
    for (char i = 'A'; i<=n ; i++)
    {
     for (char j = 'A'; j<=n ; j++ )
     {
          System.out.print(i+" ");
     }
        System.out.println();
    }
    }
}
